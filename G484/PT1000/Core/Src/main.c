float get_low_pass_filter(float value, float filtered_value, float sensitivity)
{
  return (filtered_value * (1 - sensitivity)) + (value * sensitivity);
}

#define ADC_MAX_SAMPLE_NUM (100)
static float lpf_adc[4] = {0.0, };
static uint16_t count = 0;
static volatile uint16_t adc_value[4] = {0, };
static uint32_t sum_adc[4] = {0, };

uint16_t adc_get_value(uint8_t index)
{
  if (index >= 4) {
    return 0;
  }
  return lpf_adc[index];
}

void HAL_ADC_ConvHalfCpltCallback(ADC_HandleTypeDef* hadc)
{
  if (hadc->Instance == ADC3) {
    for (int i = 0 ; i < 4 ; i++) {
      sum_adc[i] += adc_value[i];
    }

    if (count++ == ADC_MAX_SAMPLE_NUM) {
      for (int i = 0 ; i < 4 ; i++) {
        lpf_adc[i] = get_low_pass_filter((sum_adc[i] / (float)ADC_MAX_SAMPLE_NUM), lpf_adc[i], 0.1);
        sum_adc[i] = 0;
      }
      count = 0;
    }
  }
}

